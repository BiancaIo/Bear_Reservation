/*CREAREA TABELELOR*/
CREATE TABLE ORASE(cod_o NUMBER(2,0) CONSTRAINT ORASE_pk PRIMARY KEY, nume_o VARCHAR2(20) NOT NULL)
CREATE TABLE REZERVATII (cod_r NUMBER(2,0) CONSTRAINT REZERVATII_pk PRIMARY KEY, nume_r VARCHAR2(30) NOT NULL, dimensiune NUMBER(4,2) NOT NULL, cod_o NUMBER(2,0) NOT NULL, CONSTRAINT fk_ORASE FOREIGN KEY (cod_o) REFERENCES ORASE(cod_o))
CREATE TABLE ZONE (cod_z NUMBER(2,0) CONSTRAINT ZONE_pk PRIMARY KEY, locatie_z VARCHAR2(20) NOT NULL,cod_r NUMBER(2,0) NOT NULL, CONSTRAINT fk_REZERVATII FOREIGN KEY (cod_r) REFERENCES REZERVATII(cod_R))
CREATE TABLE SPECII (cod_s NUMBER(2,0) CONSTRAINT SPECII_pk PRIMARY KEY, nume_s VARCHAR2(20) NOT NULL, caracteristici_s VARCHAR2(200) NOT NULL, cod_z NUMBER(2,0) NOT NULL,CONSTRAINT fk_ZONE FOREIGN KEY (cod_z) REFERENCES ZONE(cod_z))
CREATE TABLE URSI (ID_u NUMBER(2,0) CONSTRAINT URSI_pk PRIMARY KEY, nume_u VARCHAR2(20) NOT NULL, varsta_u NUMBER(2,0) NOT NULL, cod_s NUMBER(2,0) NOT NULL, CONSTRAINT fk_SPECII FOREIGN KEY (cod_s) REFERENCES SPECII(cod_s)) 
CREATE TABLE TIPURI_MANCARURI (cod_m NUMBER(2,0) CONSTRAINT TIPURI_pk PRIMARY KEY, nume_m VARCHAR2(20) NOT NULL, ID_u NUMBER(2,0) NOT NULL, CONSTRAINT fk_URSI FOREIGN KEY (ID_u) REFERENCES URSI(ID_u))
CREATE TABLE CONSULTATII (ID_c NUMBER(2,0) CONSTRAINT CONSULTATII_pk PRIMARY KEY, data DATE NOT NULL, detalii VARCHAR2(200) NOT NULL, ID_u NUMBER(2,0) NOT NULL, CONSTRAINT fk_URSI2 FOREIGN KEY (ID_u) REFERENCES URSI(ID_u))
CREATE TABLE TRATAMENTE (cod_t NUMBER(2,0) CONSTRAINT TRATAMENTE_pk PRIMARY KEY, nume_boala VARCHAR2(30) NOT NULL, data_in DATE NOT NULL, data_sf DATE NOT NULL, ID_c NUMBER(2,0) NOT NULL, CONSTRAINT fk_CONSULTATII FOREIGN KEY (ID_c) REFERENCES CONSULTATII (ID_c))
CREATE TABLE MEDICAMENTE (cod_med NUMBER(2,0) CONSTRAINT MEDICAMENTE_pk PRIMARY KEY, nume_med VARCHAR2(20) NOT NULL, cod_t NUMBER(2,0) NOT NULL, ID_f NUMBER(2,0) NOT NULL,CONSTRAINT fk_TRATAMENTE FOREIGN KEY (cod_t) REFERENCES TRATAMENTE (cod_t))
CREATE TABLE FIRME_MEDICAMENTE (ID_f NUMBER(2,0) CONSTRAINT FIRME_pk PRIMARY KEY, nume_f VARCHAR2(20) NOT NULL, cod_med NUMBER (2,0) NOT NULL, CONSTRAINT fk_MEDICAMENTE FOREIGN KEY (cod_med) REFERENCES MEDICAMENTE (cod_med))
CREATE TABLE ANGAJATI (ID_a NUMBER(2,0) CONSTRAINT ANGAJATI_pk PRIMARY KEY, nume_a VARCHAR2(20) NOT NULL, prenume_a VARCHAR2(20) NOT NULL, telefon_a VARCHAR2(10) NOT NULL, profesie VARCHAR2(15) NOT NULL, cod_z NUMBER(2,0) NOT NULL, CONSTRAINT fk_ZONE2 FOREIGN KEY(cod_z) REFERENCES ZONE (cod_z))

